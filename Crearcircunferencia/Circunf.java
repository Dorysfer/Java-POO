
package Crearcircunferencia;

/**
 *
 * @author Dorys Erazo Ortega
 */
public class Circunf {

    private double rad;

    public Circunf() {
    }

    public Circunf(double rad) {
        this.rad = rad;
    }

    public double getRad() {
        return rad;
    }

    public void setRad(double rad) {
        this.rad = rad;
    }

    @Override
    public String toString() {
        return "Circunf{" + "rad=" + rad + '}';
    }
}
    
    